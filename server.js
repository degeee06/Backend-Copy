app.post('/webhook/hotmart', async (req, res) => {
    try {
        logger.info('üîî Webhook Hotmart recebido', { 
            event: req.body?.event,
            data: req.body?.data 
        });
        
        const { event, data } = req.body;
        
        // Verificar assinatura (opcional mas recomendado)
        const signature = req.headers['hotmart-hottok'];
        logger.info('üìù Assinatura do webhook', { signature });
        
        // Processar diferentes tipos de eventos
        switch (event) {
            case 'PURCHASE_APPROVED':
                await handlePurchaseApproved(data);
                break;
                
            case 'PURCHASE_COMPLETE':
                await handlePurchaseComplete(data);
                break;
                
            case 'PURCHASE_CANCELED':
                await handlePurchaseCanceled(data);
                break;
                
            case 'PURCHASE_REFUNDED':
                await handlePurchaseRefunded(data);
                break;
                
            case 'PURCHASE_CHARGEBACK':
                await handlePurchaseChargeback(data);
                break;
                
            default:
                logger.warn(`Evento n√£o tratado: ${event}`);
        }
        
        // Sempre retornar 200 para confirmar recebimento
        res.status(200).json({ 
            status: 'success', 
            message: 'Webhook processed successfully' 
        });
        
    } catch (error) {
        logger.error('‚ùå Erro no webhook:', error);
        res.status(200).json({ // ‚≠ê‚≠ê SEMPRE retorne 200 mesmo com erro
            status: 'error', 
            message: error.message 
        });
    }
});

