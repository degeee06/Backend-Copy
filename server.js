// ‚úÖ NOVO: Rate Limiting
const rateLimitMap = new Map();

const rateLimit = (req, res, next) => {
    const ip = req.ip || req.connection.remoteAddress;
    const now = Date.now();
    const windowMs = 15 * 60 * 1000; // 15 minutos
    const maxRequests = 50; // 50 requests por janela
    
    if (!rateLimitMap.has(ip)) {
        rateLimitMap.set(ip, { count: 1, startTime: now });
        return next();
    }
    
    const userData = rateLimitMap.get(ip);
    
    if (now - userData.startTime > windowMs) {
        // Reset window
        rateLimitMap.set(ip, { count: 1, startTime: now });
        return next();
    }
    
    if (userData.count >= maxRequests) {
        return res.status(429).json({ 
            error: 'Muitas requisi√ß√µes. Tente novamente em 15 minutos.' 
        });
    }
    
    userData.count++;
    next();
};

// ‚úÖ NOVO: Valida√ß√£o de entrada
const validateGenerateRequest = (req, res, next) => {
    const { prompt, template } = req.body;
    
    if (!prompt || typeof prompt !== 'string') {
        return res.status(400).json({ error: 'Prompt √© obrigat√≥rio e deve ser uma string' });
    }
    
    if (prompt.length > 1000) {
        return res.status(400).json({ error: 'Prompt muito longo (m√°x: 1000 caracteres)' });
    }
    
    if (prompt.trim().length === 0) {
        return res.status(400).json({ error: 'Prompt n√£o pode estar vazio' });
    }
    
    const validTemplates = ['instagram', 'facebook', 'ecommerce', 'email', 'google', 'blog'];
    if (!validTemplates.includes(template)) {
        return res.status(400).json({ error: 'Template inv√°lido' });
    }
    
    next();
};

