function getTemperature(template) {
    // Temperaturas espec√≠ficas por tipo de conte√∫do
    const temps = {
        instagram: 0.8,    // Mais criativo
        facebook: 0.7,     // Balanceado
        ecommerce: 0.6,    // Mais consistente
        email: 0.7,        // Balanceado
        blog: 0.9,         // Muito criativo para t√≠tulos
        google: 0.5        // Muito consistente (limites rigorosos)
    };
    return temps[template] || 0.7;
}

function postProcessContent(content, template) {
    // Limpeza e formata√ß√£o p√≥s-gera√ß√£o
    let processed = content
        .replace(/\*\*(.*?)\*\*/g, '$1') // Remove **bold** markdown
        .replace(/\n{3,}/g, '\n\n')      // Remove m√∫ltiplas quebras de linha
        .trim();

    // Valida√ß√µes espec√≠ficas por template
    if (template === 'google') {
        // Garantir que atende limites do Google Ads
        const lines = processed.split('\n');
        if (lines.length >= 3) {
            const title1 = lines[0].replace('T√≠tulo 1:', '').trim();
            const title2 = lines[1].replace('T√≠tulo 2:', '').trim();
            const description = lines[2].replace('Descri√ß√£o:', '').trim();
            
            if (title1.length > 30) processed = `T√≠tulo 1: ${title1.substring(0,30)}\n${lines.slice(1).join('\n')}`;
            if (title2.length > 30) processed = `${lines[0]}\nT√≠tulo 2: ${title2.substring(0,30)}\n${lines.slice(2).join('\n')}`;
            if (description.length > 90) processed = `${lines[0]}\n${lines[1]}\nDescri√ß√£o: ${description.substring(0,90)}`;
        }
    }

    return processed;
}



